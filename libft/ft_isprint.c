#include "libft.h"

int	ft_isspace(int c)
{
	if (c >= 9 && c <= 13)
		return (1);
	return (0);
}

int	ft_isprint(int c)
{
	if ((c >= 32 && c <= 126) || ft_isspace(c))
		return (1);
	return (0);
}
